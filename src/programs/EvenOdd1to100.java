package programs;

public class EvenOdd1to100 {

	public static void main(String []args)
	{
		System.out.print("Even odd for 1 to 100 range");
	    for(int i=1;i<=100;i++)
	    {
	    	if(i%2==0)
	    	{
	    		System.out.println(i +"is a even number");
	    	}
	    	else
	    	{
	    		System.out.println(i+"is odd number");
	    	}
	    }
	}
	
	
	
}
