
public class ForLoopProgram {
       public static void main(String []args)
       {
    	   System.out.println("For loop program");
//    	   1   10
//    	   2    9
//    	   3    8
//    	   .    .
//    	   .    .   
    	   int i,j;
    	   for(i=0,j=10;i<=10;i++,j--)
    	   {
    		   System.out.println(" "+i +" "+j);
    	   }
       }
	
}
